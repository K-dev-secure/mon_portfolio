    <p>Ma principale difficulté a été de comprendre les <strong>Hooks</strong> de React (les fonctions qui permettent de gérer la logique et les données du site). Au début, leur fonctionnement me paraissait complexe. En combinant la lecture de la documentation et l'analyse de vidéos explicatives, j'ai fini par en comprendre le sens global. J'ai aussi dû apprendre à gérer les erreurs d'installation des dépendances en consultant les forums et en testant différentes configurations.</p>
</div>

