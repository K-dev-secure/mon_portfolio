<div>
    <h2>Approche et objectifs de départ</h2>
    <p>Au lancement de cette version 2, je ne maîtrisais pas encore <strong>React</strong> (un outil qui permet de découper un site en composants réutilisables). Mon objectif était d'apprendre cette technologie ainsi que <strong>TypeScript</strong>, un langage qui sécurise le code. Je voulais aussi apprendre à gérer des éléments modernes comme les <strong>iFrames</strong> et l'installation de bibliothèques d'icônes.</p>
</div>

<div>
    <h2>Collaboration et méthodologie</h2>
    <p>J'ai travaillé en totale autonomie. Au départ, je ne connaissais pas bien la gestion des <strong>dépendances</strong> (les outils externes que l'on ajoute au projet). Pour progresser, j'ai alterné entre la <strong>documentation officielle de React</strong> pour la théorie et des tutoriels YouTube pour la mise en pratique, notamment pour les animations et la structure globale.</p>
</div>

<div>
    <h2>Réalisations</h2>
    <p>J'ai mis en place l'architecture de mon site et intégré des animations fluides pour améliorer l'expérience utilisateur. J'ai appris à configurer mes propres composants et à rendre mon code beaucoup plus "rangé" et modulaire. Bien que le projet soit <strong>actuellement en cours de réalisation</strong>, la base technique est aujourd'hui bien plus solide que sur ma version précédente.</p>
</div>

